package br.edu.up.util;

public class Calculadora {
	
	public double somar(double a, double b) {
		return a + b;
	}
	
	public double subtrair(double a, double b){
		return a - b;
		
	}
	
	public double multi(double a, double b){
		return a * b;
	}
	
	public double dividir(double a, double b){
		return a / b;
	}
	

}
