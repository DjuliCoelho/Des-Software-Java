package Aula01;

public class Exercicio14 {
	    public static void main(String[] args) {
	        int a1 = 10; // primeiro termo da PA
	        int n = 7; // ordem do termo desejado
	        int r = 3; // razão da PA

	        int an = a1 + (n-1) * r; // fórmula para encontrar o n-ésimo termo

	        System.out.println("O " + n + "º termo da progressão aritmética é: " + an);
	    }
	}



